function envio(formId){
    var formObj = {};
    var inputs = $('#'+formId).serializeArray();
    $.each(inputs, function (i, input) {
        formObj[input.name] = input.value;
    });
    let nombre = formObj['nombre'];
    let telefono = formObj['telefono'];
    let email = formObj['email'];
    let asunto = formObj['asunto'];
    let descripcion = formObj['descripcion'];
    $.ajax({
    type: 'GET',
    url: 'general.php',
    data: 'email='+email+'&telefono='+telefono+'&asunto='+asunto+'&descripcion='+descripcion+'&nombre='+nombre,
    async: true,
    success: function(data){
     
                if(data){
                    document.getElementById('cita').innerHTML = data;
                    setTimeout(caa, 5000);
                }
                else if(!data){ 
                console.log(data);               
                document.getElementById('cita').innerHTML = '<div class="alert noti alert-success"><strong>EMPTY!</strong></div>';
                }
                else{
                  console.log(data);
                    document.getElementById('cita').innerHTML = '<div class="alert noti alert-success"><strong>EMPTY!</strong></div>';                    
                }
                      
      
    },
    error: function(jHXR, exception, m){
      console.error(data);
    }
  });

  }

